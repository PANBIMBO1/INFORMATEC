                    <h1 align="center" class="mb-3">
                        Turismo en la Región
                    </h1>
                    <img src="turismo.png" width="100%" class="mt-1 mb-3">
                    <p align="justify">En el proceso de reactivar la economía, el departamento ha demostrado un importante avance en la reapertura del turismo, llegando incluso a superar las cifras de reactivación reportadas a nivel nacional.
                        <br>
                        <br>
                        La secretaria de desarrollo económico y competitividad, Ana María Valencia Gómez explicó que estas cifras siguen consolidando a Risaralda como uno de los principales destinos turísticos del país, gracias a su inmensa oferta de atractivos naturales, por su destacada oferta hotelera y sobre todo por la calidez de sus gentes.
                        <br>Risaralda ha estado este año en el top 10 de los destinos turísticos del país, incluso llegando en agosto a ocupar el cuarto lugar en Colombia con mayor ocupación hotelera, solo superado por los destinos de sol y playa, quedando en primer lugar en turismo de naturaleza. Además de sus atributos naturales, el departamento también ha mostrado grandes fortalezas en otros tipos de turismo como es el caso del de bienestar y salud, reuniones, aventura y turismo comunitario.
                        <br>
                        Hay que recalcar que el turismo desempeña una labor clave en el proceso de reactivación económica del departamento, por eso el Gobernador Victor Manuel Tamayo Vargas, a través de la Secretaría de Desarrollo Económico y Competitividad con su Dirección de Turismo, ha fortalecido este significativo proceso, gestionando inversiones que contribuyan a aumentar la llegada de visitantes y turistas nacionales y extranjeros.
                        <br>
                        <br>
                        la ocupación hotelera que tuvo el departamento en septiembre de este año, fue superior al registrado en el mismo mes del 2020 y 2019, lo que evidencia que la recuperación del sector va por buen camino e invitó a seguir fortaleciendo los alojamientos rurales para darle una mejor experiencia a los turistas.
                        <br>Se espera que la ocupación hotelera aumente en octubre 2021, una vez se tengan las mediciones, teniendo en cuenta fiestas municipales como las de Santa Rosa de Cabal, la semana de receso escolar, el puente festivo del 18 de octubre, la reactivación de las caravanas turísticas, entre otros aspectos.
                        <br>Los datos mencionados evidencian la importancia de que el departamento sea anfitrión de eventos de carácter nacional, ya que estos generan la llegada de cientos de visitantes y turistas que además de alojarse en los hoteles de los diferentes municipios del departamento, también dinamizan la economía de los restaurantes, centros comerciales, el comercio diurno y nocturno, cafeterías, panaderías.
