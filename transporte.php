          <h1 align="center" class="mb-3">
            ¿Como funciona el megacable?
          </h1>
          <img src="transporte.png" width="100%" class="mt-1 mb-3">
          <p align="justify">El Megacable de Pereira fue inaugurado de manera protocolaria durante el pasado mes de agosto, al evento asistieron los alcaldes de Medellín y Bogotá. El proyecto inició su ejecución desde el año 2017 y pretende ser una alternativa de movilidad para los ciudadanos de la capital de Risaralda. Las estimaciones que se han hecho, por parte de la Alcaldía de Pereira, proyectan que los recorridos se reduzcan de 40 minutos a 12 minutos para los usuarios. ¿Cómo funciona el Megacable de Pereira?
            <br>
            <br>
            Desde el pasado lunes 20 de septiembre inició el funcionamiento de las cuatro estaciones que hacen parte de esta línea del Megacable. "Desde este lunes a las ocho de la mañana el sistema de transporte tipo cable va a operar gratis por un mes. Las personas en cualquier punto del área metropolitana con la misma tarjeta y el mismo pasaje pueden llegar hasta el Megacable, así estamos garantizando un sistema de transporte multimodal", dijo Carlos Maya, alcalde de Pereira. El servicio de cable aéreo será complementario al sistema de Megabus y se podrá realizar trasbordo entre las dos alternativas de modalidad. Actualmente funcionan las estaciones del Parque Olaya Herrera, Terminal de Transportes, Universidad Tecnológica de Pereira y Barrio las Brisas. Los pasajeros podrán tomar el servicio en cualquiera de estas estaciones, el trazado del Megacable tiene una longitud de 3.4 kilómetros y es el medio de transporte de su tipo más extenso de Colombia.
            Horario y costo de pasaje del Megacable de Pereira.
            <br>
            <br>
            De acuerdo con los estudios realizados, un recorrido completo en un solo sentido, por toda la longitud del Megacable, toma aproximadamente una hora de tiempo y los usuarios pueden hacer uso del servicio entre las 5:00 am y las 11:00 pm. A lo largo de cada día el servicio tendrá una actividad de 18 horas para desplazar a todos los ciudadanos que requieren de esta alternativa de movilidad y el costo del pasaje se estima en $2.200 pesos. Entre tanto, se conoció que el Megacable de Pereira tiene la capacidad para transportar a 1.000 personas en el transcurso de una hora para cada sentido. En el primer día de operaciones se confirmó que más de 7.000 pereiranos se subieron a una de las 50 cabinas que tienen la capacidad de llevar a 10 personas. Se espera que en los próximos días se ajusten algunos detalles para progresivamente aumentar el uso de este nuevo medio de transporte.
