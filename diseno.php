                    <h2 class="mb-3 mt-3"> Diseño mecánico por Computador</h2>
                    <img src="diseno_mecanico.png" width="100%" class="mb-3">
                    <p align="justify">
                        Diseño, desarrollo y fabricación de bienes de consumo tales como electrodomésticos, sistemas de diversión y entretenimiento, dispositivos de comunicación masiva, sistemas de seguridad y entrenamiento.» Diseño, desarrollo y fabricación de equipos para agricultura, minería, exploración petrolera e implementación de sistemas de instrumentación y control en las refinerías.» Diseño, manejo y control de sistemas de producción que involucran procesos químicos para la fabricación de bebidas, alimentos, productos farmacéuticos y detergentes, entre otros.» Manejo y control de sistemas de fabricación de partes para automóviles como motores, generadores y otros. Además, de la supervisión y control de los procesos de ensamble de los mismos.» Diseño e implementación de sistemas de control e instrumentación utilizados en la generación y distribución de potencia eléctrica.» Diseño y manejo de sistemas de enfriamiento, de calentamiento, de aire acondicionado, de ventilación y demás sistemas de control ambiental.» Modelaje y simulación asistida por computador, para procesos desarrollados en ingeniería, medicina y biología aplicada, entre otros.» El ejercicio en cargos de dirección y manejo de empresas e industrias, cuya base de trabajo sea la ingeniería de punta y en especial, la automatización.
                    </p>

                    <h2 class="mb-3 mt-3"> Diseño Topográfico</h2>
                    <img src="topografia.png" width="100%" class="mb-3">
                    <p align="justify">
                        La topografía es la ciencia que estudia el conjunto de principios y procedimientos que tienen por objeto la representación gráfica de la superficie terrestre, con sus formas y detalles; tanto naturales como artificiales; (véase planimetría y altimetría)
                    </p>
                    <p align="justify">
                        Esta representación tiene lugar sobre superficies planas, limitándose a pequeñas extensiones de terreno, utilizando la denominación de «geodesia» para áreas mayores. De manera muy simple, puede decirse que para un topógrafo la Tierra es plana (geométricamente), mientras que para la geodesia no lo es.
                    </p>

                    <h2 class="mb-3 mt-3"> Diseño Arquitectonico</h2>
                    <img src="arquitectura.png" width="100%" class="mb-3">
