                    <h1 align="center" class="mb-3">
                        Dibujo Técnico y Diseño Industrial
                    </h1>
                    <img src="diseno.png" width="100%" class="mt-1 mb-3">
                    <p align="justify">
                        Uno de los objetivos principales de la especialidad es dar a conocer como se fundamenta la Arquitectura y de esta forma, ayudar a expresar el arte y la creatividad, las cuales se ven reflejadas en las construcciones para la comunidad o sociedad. Lo que permite dejar fluir la inventiva de una persona y de cierto modo compartirla con los demás.
                    </p>
                    <p align="justify">
                        En general, el Dibujo Técnico y Diseño Industrial busca satisfacer las necesidades que se le presentan a un individuo con respecto al “confort”. Por ejemplo, con el Diseño arquitectónico se busca suplir la necesidad de establecerse en espacios que sean de agrado para una persona, equilibrando lo estético con lo tecnológico. Otras personas, buscan tener bienes materiales, los cuales presentan características que los atraigan como la parte estética y buscar cualidades polifacéticas de objetos, servicios y de sus sistemas en ciclos vitales enteros; esto es solucionado desde el Diseño industrial.
                    </p>
